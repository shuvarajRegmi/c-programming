#include<stdio.h>
int main()
{
    // Create variables
    int num = 12; // Integer 
    float fnum = 59.60; //Floating point number
    char letter = 'b'; // Character
    
    // print variables
    printf("%d\n",num);
    printf("%f\n",fnum);
    printf("%c\n",letter);
    return 0;
}